namespace Savchenko\Donation\Model\Quote\Address\Total;

use Savchenko\Donation\Model\Donation as DonationModel;

/**
 * Class Donation
 * @package Savchenko\Donation\Model\Quote\Address\Total
 */
class Donation extends \Magento\Quote\Model\Quote\Address\Total\AbstractTotal
{
    /**
     * @param \Magento\Quote\Model\Quote $quote
     * @param \Magento\Quote\Api\Data\ShippingAssignmentInterface $shippingAssignment
     * @param \Magento\Quote\Model\Quote\Address\Total $total
     * @return $this
     */
    public function collect(
        \Magento\Quote\Model\Quote $quote,
        \Magento\Quote\Api\Data\ShippingAssignmentInterface $shippingAssignment,
        \Magento\Quote\Model\Quote\Address\Total $total
    )
    {
        $total->addTotalAmount(DonationModel::DONATION_KEY, $quote->getDonationAmount());
        $total->addBaseTotalAmount(DonationModel::DONATION_KEY, $quote->getDonationAmount());
        return $this;
    }

    /**
     * @param \Magento\Quote\Model\Quote $quote
     * @param \Magento\Quote\Model\Quote\Address\Total $total
     * @return array
     */
    public function fetch(\Magento\Quote\Model\Quote $quote, \Magento\Quote\Model\Quote\Address\Total $total)
    {
        return [
            'code' => $this->getCode(),
            'title' => __('Discount amount'),
            'value' => $quote->getDonationAmount()
        ];
    }
}
